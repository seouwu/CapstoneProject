package capstone;

public class PrivateDTO {
	private String student_name;
	private String p_student_number;
	private String phone_number;
	private String email;
	private String id;
	private String pw;
	private int student_grade;
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getP_student_number() {
		return p_student_number;
	}
	public void setP_student_number(String p_student_number) {
		this.p_student_number = p_student_number;
	}
	public String getPhone_number() {
		return phone_number;
	}
	public void setPhone_number(String phone_number) {
		this.phone_number = phone_number;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public int getStudent_grade() {
		return student_grade;
	}
	public void setStudent_grade(int student_grade) {
		this.student_grade = student_grade;
	}
	

	
}
